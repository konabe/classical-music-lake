  await navigateTo("/listening-logs");
}
</script>

<template>
  <div>
    <h1 class="page-title">й‘‘иіһиЁҳйҢІгӮ’иҝҪеҠ </h1>
    <ListeningLogForm submit-label="иЁҳйҢІгҒҷгӮӢ" @submit="handleSubmit" />
  </div>
